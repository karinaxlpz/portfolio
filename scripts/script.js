// NAV ACTIVE
var header = document.getElementById("nav-center");
var btns = header.getElementsByClassName("btn");
for (var i = 0; i < btns.length; i++) {
    btns[i].addEventListener("click", function() {
        var current = document.getElementsByClassName("view-mode");
        current[0].className = current[0].className.replace(" view-mode", "");
        this.className += " view-mode";
    });
}

// INTRODUCTION TOGGLE
var coll = document.getElementsByClassName("index-description-title collapsible");
var i;

for (i = 0; i < coll.length; i++) {
    coll[i].addEventListener("click", function() {
        this.classList.toggle("active");
        var content = this.nextElementSibling;
        if (content.style.display === "block") {
            content.style.display = "none";
        } else {
            content.style.display = "block";
        }
    });
}


